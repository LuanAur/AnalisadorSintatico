    private void salvarArquivo() {
        try {
            File arquivo = new File(arqCodigo);
            PrintWriter pw = new PrintWriter(arquivo);
            pw.write(jtaCodigo.getText());
            pw.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void salvarArquivoComo() {
        jfcSalvar.setDialogTitle("Salvar Arquivo");
        jfcSalvar.setCurrentDirectory(new File("."));
        int select = jfcSalvar.showSaveDialog(null);
        
        if (select == JFileChooser.APPROVE_OPTION) {
            try {
                File arquivo = jfcSalvar.getSelectedFile();
                PrintWriter pw = new PrintWriter(arquivo);
                pw.write(jtaCodigo.getText());
                pw.close();
            } catch (FileNotFoundException ex) {
                Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
