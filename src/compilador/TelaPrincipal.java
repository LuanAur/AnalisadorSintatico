    private void jmiAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmiAbrirActionPerformed
        int returnVal = jfcAbrir.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = jfcAbrir.getSelectedFile();
            arqCodigo = file.getAbsolutePath();
            try {
              // What to do with the file, e.g. display it in a TextArea
              jtaCodigo.read(new FileReader(arqCodigo), null);
            } catch (IOException ex) {
              System.out.println("Ocorreu um problema acessando o arquivo" + file.getAbsolutePath());
            }
        } else {
            System.out.println("Acesso ao arquivo cancelada pelo usu√°rio.");
        }
    }//GEN-LAST:event_jmiAbrirActionPerformed

